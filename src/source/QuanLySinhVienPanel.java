    private void tableQLSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableQLSVMouseClicked
         try {
            Connection con = DriverManager.getConnection(myURL);
            Statement s = con.createStatement();
            int r = tableQLSV.getSelectedRow();
            Object mssv = tablemodel.getValueAt(r, 0);
            ResultSet rs  = s.executeQuery("select * from SinhVien where MaSinhVien = '" + mssv + "'");
            if (rs.next()) {
                txtMaSinhVien.setText(rs.getString(1));
                txtHoTen.setText(rs.getString(2));
                txtEmail.setText(rs.getString(3));
                txtSoDienThoai.setText(rs.getString(4));
                if (rs.getInt(5) == 1)
                        rdNam.setSelected(true);
                else rdNu.setSelected(true);
                txtAreaDiaChi.setText(rs.getString(6));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_tableQLSVMouseClicked


