    void loadThongTin() {
        try {
            Connection con = DriverManager.getConnection(myURL);
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select * from SinhVien");
            tablemodel = new DefaultTableModel();
            String []titles = {"Mã Sinh Viên", "Họ Tên", "Email", "SĐT", "Giới Tính", "Địa Chỉ"};
            tablemodel.setColumnIdentifiers(titles);
            
            tableQLSV.setModel(tablemodel);
            
            while (rs.next()) {
                Object rows[] = new Object[6];
                rows[0] = rs.getString(1);
                rows[1] = rs.getString(2);
                rows[2] = rs.getString(3);
                rows[3] = rs.getString(4);
                rows[4] = rs.getInt(5);
                rows[5] = rs.getString(6);
                tablemodel.addRow(rows);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
